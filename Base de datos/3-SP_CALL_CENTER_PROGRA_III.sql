CREATE PROCEDURE SP_FILTRAR_ACTIVOS
(
	@Desc_Activo nvarchar(265)
)
AS
SELECT [Placa_Activo]
      ,[Id_TipoActivo]
      ,[Desc_Activo]
      ,[Id_MarcaActivo]
      ,[Id_Departamento_Responsable]
      ,[Prioridad_SLA]
      ,[Id_Estado]
      ,[FecCreacion]
      ,[UsuCreacion]
      ,[FecModificacion]
      ,[UsuModificacion]
  FROM [dbo].[Tbl_Activos]
  WHERE [Desc_Activo] LIKE '%' + @Desc_Activo + '%'
GO

